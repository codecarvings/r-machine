// ─── _fixture.tsx (IntelliSense probe targets) ──────────────────────────────

export interface FixtureAnchors {
  /** any OuterGear namespace (for deps + surface probes) */
  outerNs: string;
  /** any shell namespace (for the Plug consumer probe) */
  shellNs: string;
}

export function renderFixture(a: FixtureAnchors): string {
  const outer = JSON.stringify(a.outerNs);
  const shell = JSON.stringify(a.shellNs);
  // Completion probes use EMPTY strings: with the cursor inside `""` there is no
  // typed prefix, so tsserver returns the FULL candidate union — the real cost a
  // developer pays the instant they open the quote. These four lines are
  // intentional type errors (empty string is not a valid namespace); that is why
  // `_fixture.tsx` is excluded from tsconfig.compile.json (the file used for the
  // clean compile-metrics gate). It stays in the main tsconfig so tsserver loads
  // it inside the real project (preserving `@r-machine/source` resolution).
  return (
    headerComment +
    `import { OuterGear } from "./setup";\n` +
    `import { Plug } from "./toolset";\n` +
    `import { ResourceAtlas } from "./resource-atlas";\n\n` +
    `const token = ResourceAtlas.getTokenBuilder();\n\n` +
    `// Atlas-key completion — LIST mode (full union; cursor inside empty string).\n` +
    `export const probeDepsList = OuterGear.withDeps(""); /*MARK_DEPS_LIST*/\n\n` +
    `// Atlas-key completion — MAP mode.\n` +
    `export const probeDepsMap = OuterGear.withDeps({ a: "" }); /*MARK_DEPS_MAP*/\n\n` +
    `// Token-builder namespace-key completion.\n` +
    `export const probeToken = token(""); /*MARK_TOKEN*/\n\n` +
    `// Consumer public-surface completion inside Plug().\n` +
    `export const probePlug = Plug(""); /*MARK_PLUG*/\n\n` +
    `// Dependency-surface member completion (valid deps; cursor after \`d.\`).\n` +
    `export const probeSurface = OuterGear.withDeps(${outer}).define((plugin, _) => { const [d] = plugin; return { x: _.getter(() => d.peek) }; }); /*MARK_SURFACE*/\n\n` +
    `// Hover / quickinfo on a valid consumer plug value.\n` +
    `export const probeValid = Plug(${shell});\n` +
    `export const probeHover = probeValid; /*MARK_HOVER*/\n\n` +
    `// Scratch line: the driver mutates it before each sample to bump the file\n` +
    `// version and invalidate tsserver's completion cache — so every sample\n` +
    `// measures a real recompute (what a keystroke costs), not a cache hit.\n` +
    `//SCRATCH\n`
  );
}

// ─── tsconfig.json (the project actually measured) ──────────────────────────

const COMPILER_OPTIONS = {
  target: "ES2020",
  lib: ["ES2020", "ESNext.Disposable", "DOM", "DOM.Iterable"],
  module: "ESNext",
  moduleResolution: "Bundler",
  customConditions: ["@r-machine/source"],
  jsx: "react-jsx",
  strict: true,
  exactOptionalPropertyTypes: true,
  skipLibCheck: true,
  noEmit: true,
  isolatedModules: true,
  types: [] as string[],
};

/**
 * Main project config — used by tsserver (it auto-discovers `tsconfig.json`).
 * Includes `_fixture.tsx` so probes load inside the REAL project, preserving
 * `@r-machine/source` resolution (the exact generics a user sees in-editor).
 * moduleResolution "bundler" mirrors how real R-Machine apps resolve (Vite/Next).
 */
export function renderTsconfig(): string {
  return `${JSON.stringify({ compilerOptions: COMPILER_OPTIONS, include: ["src"] }, null, 2)}\n`;
}

/**
 * Compile-metrics config — excludes `_fixture.tsx` (whose probe strings are
 * intentional errors) so the compile run measures only the real resources and
 * the type-check gate stays clean.
 */
export function renderCompileTsconfig(): string {
  return `${JSON.stringify(
    { compilerOptions: COMPILER_OPTIONS, include: ["src"], exclude: ["src/_fixture.tsx"] },
    null,
    2
  )}\n`;
}
