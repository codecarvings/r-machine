/**
 * Pure string renderers for one generated R-Machine project.
 *
 * Every template mirrors a REAL pattern verified against examples/react:
 *  - OuterGear.withState({...}).define((plugin, _) => { const { $ } = plugin; … })  (timer.ts)
 *  - OuterGear.withDeps(...).define((plugin, _) => { const [d] = plugin; … })       (operator.ts)
 *  - BaseGear.withDeps(...).define((plugin) => { const [d] = plugin; … })           (no `_` cursor: base is stateless)
 *  - plain-object Shell with en/it variants                                         (shell/common/en.tsx)
 *  - Shell.define((plugin) => { const { fmt } = plugin; … }) shell(mono) kit        (shell/features/intl_demo/en.tsx)
 *  - shell(mono) Intl formatter via `$` destructured from plugin                    (shell/lib/fmt.ts)
 *  - resource-atlas via defineLayout + class extends folders<Map> (resource-atlas.ts)
 *  - tokens via ResourceAtlas.getTokenBuilder()                   (resource-atlas.ts token usage)
 *
 * Deps are rendered in BOTH list and map mode, with BOTH string and token
 * references, to exercise the distinct ListPlugin/MapPlugin + token type paths.
 * Shell members include strings, functions, and JSX fragments.
 */

export type Kind = "outerStateful" | "outerDeps" | "base" | "shell" | "fmt";
export type DepMode = "list" | "map";
export type RefMode = "string" | "token";

export interface ResSpec {
  index: number;
  kind: Kind;
  /** Atlas key / namespace, may carry a leading `#` internal marker (e.g. "#base/b3"). */
  ns: string;
  /** Exported type name, e.g. "Outer_G3". */
  typeName: string;
  /** Import path relative to src root, no extension (e.g. "outer/g3" or "shell/grp0/s5/en"). */
  importPath: string;
  /** Directory depth of the source file under src/ (for relative ../setup imports). */
  depth: number;
  /** Local identifier when consumed as a dependency, e.g. "g3" / "b2". */
  varName: string;
  deps: ResSpec[];
  depMode: DepMode;
  refMode: RefMode;
  width: number;
  /** Shells only: true ⇒ `Shell.define((plugin) => { const { fmt } = plugin; … })` (kit-using); false ⇒ plain object. */
  useDefine: boolean;
}

const headerComment = "// AUTO-GENERATED by type-scale-bench. Do not edit. Re-run `pnpm generate`.\n\n";

/** `.peek` accessor expression for reading a dependency's canonical member. */
function depAccessor(dep: ResSpec): string {
  // OuterGears expose `peek` as a reactive getter; `SurfaceItem<Getter<V>> = V`,
  // so on the surface it reads back as the bare value (`.peek`, a number).
  // BaseGears expose `peek` as a plain function, surfaced as-is (`.peek()`).
  return dep.kind === "base" ? `${dep.varName}.peek()` : `${dep.varName}.peek`;
}

function tokenRef(ns: string): string {
  return `T[${JSON.stringify(ns)}]`;
}

/** Render the argument list passed to `.withDeps(...)` / `Plug(...)`. */
function renderDepArgs(deps: ResSpec[], depMode: DepMode, refMode: RefMode): string {
  const ref = (ns: string) => (refMode === "token" ? tokenRef(ns) : JSON.stringify(ns));
  if (depMode === "list") {
    return deps.map((d) => ref(d.ns)).join(", ");
  }
  return `{ ${deps.map((d) => `${d.varName}: ${ref(d.ns)}`).join(", ")} }`;
}

/** Render the `const … = plugin;` line that destructures the deps in the body (form A). */
function renderDepsDestructure(deps: ResSpec[], depMode: DepMode): string {
  const names = deps.map((d) => d.varName).join(", ");
  const bound = depMode === "list" ? `[${names}]` : `{ ${names} }`;
  return `  const ${bound} = plugin;\n`;
}

function setupImport(spec: ResSpec, names: string): string {
  const rel = "../".repeat(spec.depth) || "./";
  return `import { ${names} } from "${rel}setup";`;
}

function tokensImport(spec: ResSpec): string {
  const rel = "../".repeat(spec.depth) || "./";
  return `import { T } from "${rel}tokens";`;
}

// ─── OuterGear (stateful leaf) ──────────────────────────────────────────────

export function renderOuterStateful(spec: ResSpec): string {
  const extra: string[] = [];
  for (let i = 4; i < spec.width; i++) {
    extra.push(`  m${i}: _.getter(() => $.state.v1 + ${i}),`);
  }
  return (
    headerComment +
    `${setupImport(spec, "OuterGear, type RShape")}\n\n` +
    `export const r = OuterGear.withState({\n` +
    `  v0: 0,\n  v1: 0,\n  flag: false,\n` +
    `}).define((plugin, _) => {\n` +
    `  const { $ } = plugin;\n` +
    `  return {\n` +
    `  peek: _.getter(() => $.state.v0),\n` +
    `  sum: _.getter(() => $.state.v0 + $.state.v1),\n` +
    `  bump: _.action(() => ({ v0: $.state.v0 + 1 })),\n` +
    `  toggle: _.action(() => ({ flag: !$.state.flag })),\n` +
    extra.join("\n") +
    (extra.length ? "\n" : "") +
    `  };\n});\n\n` +
    `export type ${spec.typeName} = RShape<typeof r>;\n`
  );
}

// ─── OuterGear (stateless connector, with deps) ─────────────────────────────

export function renderOuterDeps(spec: ResSpec): string {
  const sum = spec.deps.map(depAccessor).join(" + ") || "0";
  const args = renderDepArgs(spec.deps, spec.depMode, spec.refMode);
  const destructure = renderDepsDestructure(spec.deps, spec.depMode);
  const extra: string[] = [];
  for (let i = 1; i < spec.width; i++) {
    extra.push(`  m${i}: () => ${spec.index} + ${i},`);
  }
  const tokenLine = spec.refMode === "token" ? `${tokensImport(spec)}\n` : "";
  return (
    headerComment +
    `${setupImport(spec, "OuterGear, type RShape")}\n${tokenLine}\n` +
    `export const r = OuterGear.withDeps(${args}).define((plugin, _) => {\n` +
    destructure +
    `  return {\n` +
    `  peek: _.getter(() => ${sum}),\n` +
    extra.join("\n") +
    (extra.length ? "\n" : "") +
    `  };\n});\n\n` +
    `export type ${spec.typeName} = RShape<typeof r>;\n`
  );
}

// ─── BaseGear (stateless; no `_` cursor) ────────────────────────────────────

export function renderBase(spec: ResSpec): string {
  const extra: string[] = [];
  for (let i = 1; i < spec.width; i++) {
    extra.push(`  k${i}: () => ${spec.index} + ${i},`);
  }
  const body = `  peek: () => ${spec.deps.map(depAccessor).join(" + ") || "1"},\n${extra.join("\n")}${extra.length ? "\n" : ""}`;

  if (spec.deps.length === 0) {
    return (
      headerComment +
      `${setupImport(spec, "BaseGear, type RShape")}\n\n` +
      `export const r = BaseGear.define(() => ({\n${body}}));\n\n` +
      `export type ${spec.typeName} = RShape<typeof r>;\n`
    );
  }

  const args = renderDepArgs(spec.deps, spec.depMode, spec.refMode);
  const destructure = renderDepsDestructure(spec.deps, spec.depMode);
  const tokenLine = spec.refMode === "token" ? `${tokensImport(spec)}\n` : "";
  return (
    headerComment +
    `${setupImport(spec, "BaseGear, type RShape")}\n${tokenLine}\n` +
    `export const r = BaseGear.withDeps(${args}).define((plugin) => {\n${destructure}  return {\n${body}  };\n});\n\n` +
    `export type ${spec.typeName} = RShape<typeof r>;\n`
  );
}

// ─── Shell ──────────────────────────────────────────────────────────────────
// Two real flavours, each with a canonical `en` variant + a localized `it` variant:
//  - plain object  (shell/common/en.tsx): strings + a function + a static JSX fragment
//  - Shell.define  (shell/features/intl_demo/en.tsx): same, but the function and JSX
//    use the `fmt` shell(mono) kit destructured from the define plugin.
//
// Locale variants follow the real pattern (examples/next-with-app-path-strategy):
//  - `en` (canonical): exports `r` AND `export type Shell_X = RShape<typeof r>`.
//  - `it` (localized): wraps the object in `localized("<ns>", { … })` so it is
//    checked against the canonical shape, and does NOT re-export the type.
// The atlas imports the type from the `en` file only.

const SHELL_KEYS = ["title", "subtitle", "body", "cta", "note", "label"];

/** Shared string members, sized to spec.width minus the fn + JSX members. */
function shellStringLines(spec: ResSpec, locale: "en" | "it"): string {
  const keys = SHELL_KEYS.slice(0, Math.max(2, spec.width - 2));
  return keys.map((k) => `  ${k}: ${JSON.stringify(`${spec.typeName} ${k} (${locale})`)},`).join("\n");
}

export function renderShellVariant(spec: ResSpec, locale: "en" | "it"): string {
  const rel = "../".repeat(spec.depth) || "./";
  const body =
    `${shellStringLines(spec, locale)}\n` +
    // function member
    `  greet: (name: string) => "Hello " + name + " — ${spec.typeName} (${locale})",\n` +
    // JSX fragment member
    `  badge: (\n    <span className="badge">\n      ${spec.typeName} <strong>(${locale})</strong>\n    </span>\n  ),\n`;

  if (locale === "en") {
    return (
      headerComment +
      `import type { RShape } from "${rel}setup";\n\n` +
      `export const r = {\n${body}};\n\n` +
      `export type ${spec.typeName} = RShape<typeof r>;\n`
    );
  }
  return (
    headerComment +
    `import { localized } from "${rel}setup";\n\n` +
    `export const r = localized(${JSON.stringify(spec.ns)}, {\n${body}});\n`
  );
}

export function renderShellDefineVariant(spec: ResSpec, locale: "en" | "it"): string {
  const rel = "../".repeat(spec.depth) || "./";
  const body =
    `${shellStringLines(spec, locale)}\n` +
    // function member that uses the locale-aware fmt kit
    `  price: (amount: number) => fmt.currency(amount) + " — ${spec.typeName} (${locale})",\n` +
    // JSX fragment member that embeds fmt output
    `  summary: (amount: number, when: Date) => (\n    <>\n      <strong>{fmt.currency(amount)}</strong> on {fmt.date.long(when)} — ${spec.typeName} (${locale})\n    </>\n  ),\n`;

  if (locale === "en") {
    return (
      headerComment +
      `import { type RShape, Shell } from "${rel}setup";\n\n` +
      `export const r = Shell.define((plugin) => {\n  const { fmt } = plugin;\n  return {\n${body}  };\n});\n\n` +
      `export type ${spec.typeName} = RShape<typeof r>;\n`
    );
  }
  return (
    headerComment +
    `import { localized, Shell } from "${rel}setup";\n\n` +
    `export const r = Shell.define((plugin) => {\n  const { fmt } = plugin;\n  return localized(${JSON.stringify(spec.ns)}, {\n${body}  });\n});\n`
  );
}

/** The shell(mono) Intl formatter at shell/lib/fmt.ts (single, locale-aware file). */
export function renderFmtShell(spec: ResSpec): string {
  const rel = "../".repeat(spec.depth) || "./";
  return (
    headerComment +
    `import { type Locale, type RShape, Shell } from "${rel}setup";\n\n` +
    `const currencyByLocale: Record<Locale, string> = { en: "USD", it: "EUR" };\n\n` +
    `export const r = Shell.define((plugin) => {\n` +
    `  const { $ } = plugin;\n` +
    `  const locale = $.locale;\n` +
    `  const dateLong = new Intl.DateTimeFormat(locale, { dateStyle: "long" });\n` +
    `  const numberFmt = new Intl.NumberFormat(locale);\n` +
    `  const currencyFmt = new Intl.NumberFormat(locale, {\n` +
    `    style: "currency",\n` +
    `    currency: currencyByLocale[locale] ?? "USD",\n` +
    `  });\n` +
    `  const pluralRules = new Intl.PluralRules(locale);\n` +
    `  return {\n` +
    `    date: { long: (d: Date) => dateLong.format(d) },\n` +
    `    number: (n: number) => numberFmt.format(n),\n` +
    `    currency: (n: number) => currencyFmt.format(n),\n` +
    `    plural: (count: number, one: string, other: string) =>\n` +
    `      count + " " + (pluralRules.select(count) === "one" ? one : other),\n` +
    `  };\n` +
    `});\n\n` +
    `export type ${spec.typeName} = RShape<typeof r>;\n`
  );
}

// ─── resource-atlas.ts ──────────────────────────────────────────────────────

export function renderResourceAtlas(specs: ResSpec[]): string {
  const imports = specs.map((s) => `import type { ${s.typeName} } from "./${s.importPath}";`).join("\n");
  const mapEntries = specs.map((s) => `  ${JSON.stringify(s.ns)}: ${s.typeName};`).join("\n");
  return (
    headerComment +
    `import { defineLayout } from "r-machine";\n${imports}\n\n` +
    `const folders = defineLayout({\n` +
    `  "base/": "gear:base",\n` +
    `  "outer/": "gear:outer",\n` +
    `  "shell/": "shell",\n` +
    `  "shell/lib/": "shell(mono)",\n` +
    `});\n\n` +
    `type ResourceMap = {\n${mapEntries}\n};\n\n` +
    `export class ResourceAtlas extends folders<ResourceMap>() {}\n`
  );
}

// ─── tokens.ts ──────────────────────────────────────────────────────────────

export function renderTokens(specs: ResSpec[]): string {
  const entries = specs.map((s) => `  ${JSON.stringify(s.ns)}: token(${JSON.stringify(s.ns)}),`).join("\n");
  return (
    headerComment +
    `import { ResourceAtlas } from "./resource-atlas";\n\n` +
    `const token = ResourceAtlas.getTokenBuilder();\n\n` +
    `export const T = {\n${entries}\n};\n`
  );
}

